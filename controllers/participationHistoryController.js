const ParticipationHistoryService = require('../services/participationHistoryService');
const { validationResult } = require('express-validator');

/**
 * Registrar participação em atividade
 */
const recordParticipation = async (req, res) => {
  try {
    // Verificar erros de validação
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({
        success: false,
        message: 'Dados inválidos',
        errors: errors.array()
      });
    }

    const { volunteerId, activityId, institutionId, status, notes } = req.body;

    const result = await ParticipationHistoryService.recordParticipation(
      volunteerId,
      activityId,
      institutionId,
      status,
      notes
    );

    if (!result.success) {
      return res.status(400).json({
        success: false,
        message: result.error
      });
    }

    res.json({
      success: true,
      message: 'Participação registrada com sucesso',
      data: result.data
    });

  } catch (error) {
    console.error('Erro ao registrar participação:', error);
    res.status(500).json({
      success: false,
      message: 'Erro interno do servidor',
      error: process.env.NODE_ENV === 'development' ? error.message : undefined
    });
  }
};

